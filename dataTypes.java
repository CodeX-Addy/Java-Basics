
class Datatypes{
    public static void main(String []args){
        //INTEGERS
        int a = 5;
        short b = 2;
        long c = 8;
        byte d = 6;
        
        System.out.println(a);
        System.out.println(b);
        System.out.print("\n");
        System.out.println(c);
        System.out.print(d);
        
        System.out.print("\n");
        //Float
        float f = 1.6f;
        double e = 1.6;
        System.out.println(f);
        System.out.println(e);
    }
}

